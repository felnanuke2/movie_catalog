import 'package:movie_catalog/moviescreen/model/movie_model_detailed.dart';

class TvModel {
  String? backdropPath;
  List<CreatedBy>? createdBy;
  int? episodeRunTime;
  String? firstAirDate;
  List<Genres>? genres;
  String? homepage;
  int? id;
  bool? inProduction;
  List<String>? languages;
  String? lastAirDate;
  LastEpisodeToAir? lastEpisodeToAir;
  String? name;

  List<Networks>? networks;
  int? numberOfEpisodes;
  int? numberOfSeasons;
  List<String>? originCountry;
  String? originalLanguage;
  String? originalName;
  String? overview;
  num? popularity;
  String? posterPath;
  List<ProductionCompanies>? productionCompanies;
  List<ProductionCountries>? productionCountries;
  List<Seasons>? seasons;
  List<SpokenLanguages>? spokenLanguages;
  String? status;
  String? tagline;
  String? type;
  num? voteAverage;
  int? voteCount;

  TvModel(
      {this.backdropPath,
      this.createdBy,
      this.episodeRunTime,
      this.firstAirDate,
      this.genres,
      this.homepage,
      this.id,
      this.inProduction,
      this.languages,
      this.lastAirDate,
      this.lastEpisodeToAir,
      this.name,
      this.networks,
      this.numberOfEpisodes,
      this.numberOfSeasons,
      this.originCountry,
      this.originalLanguage,
      this.originalName,
      this.overview,
      this.popularity,
      this.posterPath,
      this.productionCompanies,
      this.productionCountries,
      this.seasons,
      this.spokenLanguages,
      this.status,
      this.tagline,
      this.type,
      this.voteAverage,
      this.voteCount});

  TvModel.fromJson(Map<String, dynamic> json) {
    backdropPath = json['backdrop_path'];
    if (json['created_by'] != null) {
      createdBy = List.from(json['created_by']).map((e) => CreatedBy.fromJson(e)).toList();
    }
    num episodeavarage = 0;
    List.from(json['episode_run_time']).forEach((element) {
      episodeavarage += element;
    });
    episodeRunTime = (episodeavarage / List.from(json['episode_run_time']).length).round();
    firstAirDate = json['first_air_date'];
    if (json['genres'] != null) {
      genres = List.from(json['genres']).map((e) => Genres.fromJson(e)).toList();
    }
    homepage = json['homepage'];
    id = json['id'];
    inProduction = json['in_production'];
    languages = json['languages'].cast<String>();
    lastAirDate = json['last_air_date'];
    lastEpisodeToAir = json['last_episode_to_air'] != null
        ? new LastEpisodeToAir.fromJson(json['last_episode_to_air'])
        : null;
    name = json['name'];

    if (json['networks'] != null) {
      networks = List.from(json['networks']).map((e) => Networks.fromJson(e)).toList();
    }
    numberOfEpisodes = json['number_of_episodes'];
    numberOfSeasons = json['number_of_seasons'];
    originCountry = json['origin_country'].cast<String>();
    originalLanguage = json['original_language'];
    originalName = json['original_name'];
    overview = json['overview'];
    popularity = json['popularity'];
    posterPath = json['poster_path'];
    if (json['production_companies'] != null) {
      productionCompanies = List.from(json['production_companies'])
          .map((e) => ProductionCompanies.fromJson(e))
          .toList();
    }
    if (json['production_countries'] != null) {
      productionCountries = List.from(json['production_countries'])
          .map((e) => ProductionCountries.fromJson(e))
          .toList();
    }
    if (json['seasons'] != null) {
      seasons = List.from(json['seasons']).map((e) => Seasons.fromJson(e)).toList();
    }
    if (json['spoken_languages'] != null) {
      spokenLanguages =
          List.from(json['spoken_languages']).map((e) => SpokenLanguages.fromJson(e)).toList();
    }
    status = json['status'];
    tagline = json['tagline'];
    type = json['type'];
    voteAverage = json['vote_average'];
    voteCount = json['vote_count'];
  }
}

class CreatedBy {
  int? id;
  String? creditId;
  String? name;
  int? gender;
  String? profilePath;

  CreatedBy({this.id, this.creditId, this.name, this.gender, this.profilePath});

  CreatedBy.fromJson(Map<String, dynamic> json) {
    id = json['id'];
    creditId = json['credit_id'];
    name = json['name'];
    gender = json['gender'];
    profilePath = json['profile_path'];
  }
}

class Genres {
