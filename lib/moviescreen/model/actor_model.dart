class CreditModel {
  int? id;
  List<Cast>? cast;
  List<Crew>? crew;

  CreditModel({this.id, this.cast, this.crew});

  CreditModel.fromJson(Map<String, dynamic> json) {
    id = json['id'];
    print(id!);
    if (json['cast'] != null) {
      cast = List.from(json['cast']).map((e) => Cast.fromJson(e)).toList();
    }
    if (json['crew'] != null) {
      crew = List.from(json['crew']).map((e) => Crew.fromJson(e)).toList();
    }
  }
}

